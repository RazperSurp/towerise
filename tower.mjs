class Tower{
    constructor(towerName, attackSpeed, range, damage, status, multishot, splash, asset, cost, profit, towerLimit){
        this.TOWERNAME = towerName
        this.ATTACKSPEED = attackSpeed
        this.RANGE = range
        this.DAMAGE = damage
        this.STATUS = status
        this.MULTISHOT = multishot
        this.SPLASH = splash
        this.ASSET = asset
        this.COST = cost
        this.PROFIT = profit
        this.LIMIT = towerLimit
    }
}